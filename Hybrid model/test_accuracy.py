def evaluate_model_with_confusion_matrix(test_data):
    """
    Evaluates the model and plots a confusion matrix.
    :param test_data: List of tuples (sentence, actual_sentiment)
    """
    y_true = []
    y_pred = []

    for text, actual_sentiment in test_data:
        predicted_sentiment, _, _ = classify_text(text)
        print(f"Text: {text}")  # No Unicode errors due to reconfigured encoding
        print(f"Predicted Sentiment: {predicted_sentiment}, Actual Sentiment: {actual_sentiment}\n")
        y_true.append(actual_sentiment)
        y_pred.append(predicted_sentiment)
    
    # Generate the confusion matrix
    labels = ["Positive", "Negative", "Neutral"]
    cm = confusion_matrix(y_true, y_pred, labels=labels)

    # Plot confusion matrix
    plt.figure(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues", xticklabels=labels, yticklabels=labels)
    plt.xlabel("Predicted Sentiments")
    plt.ylabel("Actual Sentiments")
    plt.title("Confusion Matrix")
    plt.show()

    # Display additional metrics
    print(classification_report(y_true, y_pred, target_names=labels))

